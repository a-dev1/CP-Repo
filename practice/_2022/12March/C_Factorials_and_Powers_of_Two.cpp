ll power(ll b, int pow) {
  if (pow == 0) return 1;

  if (pow % 2 == 0)
    return (power(b, pow / 2) * power(b, pow / 2));
  else
    return (power(b, pow / 2) * power(b, pow / 2) * b);
}

