string ans = "";
void simple_tokenizer(string s) {
  stringstream ss(s);
  string word;
  while (ss >> word) {
    // cout << word << endl;
    if (word.length() > 2) {
      ans += toupper(word[0]);
      string tp = word.substr(1, word.length() - 1);
      transform(tp.begin(), tp.end(), tp.begin(), ::tolower);
      tp.push_back(' ');
      ans += tp;
    } else {
      transform(word.begin(), word.end(), word.begin(), ::tolower);
      word.push_back(' ');
      ans += word;
    }
    // cout << ans << endl;
  }
}

