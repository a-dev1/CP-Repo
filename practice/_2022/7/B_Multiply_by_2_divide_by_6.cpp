signed main() {
  int t;
  cin >> t;
  while (t--) {
    ll n, cnt = 0LL;
    cin >> n;
    while (n >= 6LL) {
      if (n % 6LL == 0LL) {
        n = n / 6LL;
        cnt++;
      } else {
        n = n * 2LL;
        cnt++;
      }
    }
    if (n == 1LL && cnt == 0LL) {
      cout << 0 << '\n';
    } else if (n == 1LL && cnt > 0LL) {
      cout << cnt << '\n';
    } else if (n == 3LL) {
      cout << (cnt + 2LL) << '\n';
    } else
      cout << -1 << '\n';
    // debug(n);
    // debug(cnt);
  }
  return 0;
}

