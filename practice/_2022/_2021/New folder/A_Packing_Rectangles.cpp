bool isEnough(ll x) { return (((x / w) * (x / h)) >= n); }

signed main() {
  cin >> w >> h >> n;

  ll low = 0, high = 1, mid;

  while (!isEnough(high)) high *= 2;

  // mid = (low + high + 1) / 2;

  while (low + 1 < high) {
    // debug(low)
    // debug(high)
    mid = (low + high) / 2;
    // debug(mid)
    // debug(isEnough(mid))
    if (isEnough(mid)) {
      high = mid;
    } else
      low = mid;
  }

  cout << high;
  return 0;
}

