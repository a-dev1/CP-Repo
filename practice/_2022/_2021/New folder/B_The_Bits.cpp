signed main() {
  ll n;
  cin >> n;
  string str, str1;
  cin >> str >> str1;

  ll c00 = 0LL, c11 = 0LL, c01 = 0LL, c10 = 0LL;
  for (int i = 0; i < n; i++) {
    // cout << "there";
    if (str[i] == '0' && str1[i] == '0')
      c00++;
    else if (str[i] == '1' && str1[i] == '1')
      c11++;
    else if (str[i] == '0' && str1[i] == '1')
      c01++;
    else if (str[i] == '1' && str1[i] == '0')
      c10++;
  }

  cout << (c00 * c11) + (c01 * c10) + (c00 * c10);
  return 0;
}

