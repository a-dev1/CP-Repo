ll toggleBits(ll num) {
  if (num == 0LL) {
    return 1LL;
  }
  ll solution = 0LL;
  ll nextSetBit = 1LL;
  while (num != 0LL) {
    ll lastBit = (num & 1LL);
    if (lastBit == 0LL) {
      solution |= nextSetBit;
    }
    nextSetBit <<= 1LL;
    num >>= 1LL;
  }
  return solution;
}
