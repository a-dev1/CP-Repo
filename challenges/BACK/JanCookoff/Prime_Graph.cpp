bool isPrime(int n) {
  if (n == 2) {
    return true;
  }
  for (int i = 2; i <= ceil(sqrt(n)); i++) {
    if (n % i == 0) {
      return false;
    }
  }
  return true;
}
int getNearestPrimeNumber(int n) {
  for (int i = n - 1; i > 1; i--) {
    if (isPrime(i)) {
      return i;
    }
  }
  return -1;
}

